import java.lang.Math;

public class funciones {

    public double log(double x){
        double rst = Math.log(x);
        return rst;
    }

    public double sin(double x){
        double rst = Math.sin(x);
        return rst;
    }

    public double cos(double x){
        double rst = Math.cos(x);
        return rst;
    }

    public double tan(double x){
        double rst = Math.tan(x);
        return rst;
    }

    public double exp(int numero){
        double potencia = Math.E;
        double rst = Math.pow(numero,potencia);
        return rst;
    }

    public double acos(double x){
        double rst = Math.acos(x);
        return rst;
    }

    public double asin(double x) {
        double rst = Math.asin(x);
        return rst;
    }

    public double atan(double x){
        double rst = Math.atan(x);
        return rst;
    }

    public double sqrt(int x){
        double rst = Math.sqrt(x);
        return rst;
    }


}
