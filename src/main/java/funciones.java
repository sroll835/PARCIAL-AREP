import java.lang.Math;

public class funciones {

    public static double log(double x){
        double rst = Math.log(x);
        return rst;
    }

    public static double sin(double x){
        double rst = Math.sin(x);
        return rst;
    }

    public static double cos(double x){
        double rst = Math.cos(x);
        return rst;
    }

    public static double tan(double x){
        double rst = Math.tan(x);
        return rst;
    }

    public static double exp(int numero){
        double potencia = Math.E;
        double rst = Math.pow(numero,potencia);
        return rst;
    }

    public static double acos(double x){
        double rst = Math.acos(x);
        return rst;
    }

    public static double asin(double x) {
        double rst = Math.asin(x);
        return rst;
    }

    public static double atan(double x){
        double rst = Math.atan(x);
        return rst;
    }

    public static double sqrt(double x){
        double rst = Math.sqrt(x);
        return rst;
    }


}
